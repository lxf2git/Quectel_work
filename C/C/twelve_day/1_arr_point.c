#include<stdio.h>
//变量 数组 函数 指针 数组指针 指针数组 函数指针 函数指针数组
//数组指针：本质是指针 这个指针指向一个数组

void fun(int (*p)[3],int r)//int (*)[3]
{
	int i,j;
	*(*(p+1)+1) = 10;//
	*(p+1)[1] = 20;//(p+1)[1]=>*(p+1+1)=>**(p+1+1)=**（p+2）

	printf("**(p+2):%d\n",**(p+2));
	for(i=0;i<r;i++)
	{
		for(j=0;j<3;j++)
		{
			printf("%d",*(*(p+i)+j));
		}
	}
}
int main()
{
	
	int a[2][3]={1,2,3,4,5,6};
	fun(a,2);//int (*)[3]
	return 0;

}
